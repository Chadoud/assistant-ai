#!/usr/bin/env node
/**
 * Build gmail_oauth_client.json + .env.release (GMAIL_OAUTH_CLIENT_JSON_B64) from backend/.env.
 * Both outputs are gitignored — run before `npm run package:mac` or paste B64 into GitHub Actions secrets.
 */
const fs = require("fs");
const path = require("path");

const ROOT = path.join(__dirname, "..");
const BACKEND_ENV = path.join(ROOT, "backend", ".env");
const GMAIL_JSON = path.join(ROOT, "electron", "resources", "gmail_oauth_client.json");
const RELEASE_ENV = path.join(ROOT, ".env.release");

const DEFAULT_PROJECT_ID = "openjarvis-493910";
const DEFAULT_REDIRECT_URI = "http://127.0.0.1:8789/callback";

/** @param {string} filePath */
function parseDotenv(filePath) {
  if (!fs.existsSync(filePath)) {
    throw new Error(`Missing ${filePath} — add EXOSITES_GOOGLE_CLIENT_ID and EXOSITES_GOOGLE_CLIENT_SECRET`);
  }
  const out = {};
  for (const line of fs.readFileSync(filePath, "utf8").split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eq = trimmed.indexOf("=");
    if (eq <= 0) continue;
    const key = trimmed.slice(0, eq).trim();
    let value = trimmed.slice(eq + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    out[key] = value;
  }
  return out;
}

function main() {
  const env = parseDotenv(BACKEND_ENV);
  const clientId = (env.EXOSITES_GOOGLE_CLIENT_ID || "").trim();
  const clientSecret = (env.EXOSITES_GOOGLE_CLIENT_SECRET || "").trim();
  if (!clientId || !clientSecret) {
    throw new Error(
      "backend/.env must define EXOSITES_GOOGLE_CLIENT_ID and EXOSITES_GOOGLE_CLIENT_SECRET (Desktop Gmail OAuth client)"
    );
  }

  const redirectUri = (env.EXOSITES_GMAIL_OAUTH_REDIRECT_URI || DEFAULT_REDIRECT_URI).trim();
  const projectId = (env.EXOSITES_GCP_PROJECT_ID || DEFAULT_PROJECT_ID).trim();

  const payload = {
    installed: {
      client_id: clientId,
      project_id: projectId,
      auth_uri: "https://accounts.google.com/o/oauth2/auth",
      token_uri: "https://oauth2.googleapis.com/token",
      auth_provider_x509_cert_url: "https://www.googleapis.com/oauth2/v1/certs",
      client_secret: clientSecret,
      redirect_uris: [redirectUri],
    },
  };

  const jsonText = `${JSON.stringify(payload, null, 2)}\n`;
  fs.mkdirSync(path.dirname(GMAIL_JSON), { recursive: true });
  fs.writeFileSync(GMAIL_JSON, jsonText, { mode: 0o600 });

  const b64 = Buffer.from(jsonText, "utf8").toString("base64");
  const releaseEnv = [
    "# Generated by: node scripts/sync-gmail-oauth-release-env.js",
    "# Gitignored — do not commit. Used by prepare-release-resources.sh for local packaging.",
    `GMAIL_OAUTH_CLIENT_JSON_B64=${b64}`,
    "",
  ].join("\n");
  fs.writeFileSync(RELEASE_ENV, releaseEnv, { mode: 0o600 });

  console.log("[sync-gmail-oauth-release-env] Wrote electron/resources/gmail_oauth_client.json");
  console.log("[sync-gmail-oauth-release-env] Wrote .env.release (GMAIL_OAUTH_CLIENT_JSON_B64)");
  console.log("[sync-gmail-oauth-release-env] For CI: gh secret set GMAIL_OAUTH_CLIENT_JSON_B64 < .env.release");
}

main();

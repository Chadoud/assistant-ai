/**
 * macOS only: copy Electron.app, set Dock name + icon (EXO), strip quarantine,
 * and wrap the executable so cold-start via exo:// still loads this project.
 *
 * Why the wrapper exists:
 * `ELECTRON_OVERRIDE_DIST_PATH` brands Dock as "Exo", but macOS then binds the
 * `exo://` scheme to that Electron.app bundle. On macOS,
 * `setAsDefaultProtocolClient(protocol, path, args)` ignores path/args — so a
 * protocol click launches Electron.app with no project path and shows the
 * default Electron splash. The wrapper always injects the repo root first.
 *
 * Uses ELECTRON_OVERRIDE_DIST_PATH — see node_modules/electron/index.js
 */
const fs = require("fs");
const path = require("path");
const { execSync } = require("child_process");

const root = path.join(__dirname, "..");
const devMac = path.join(root, "electron", "dev-macos");
const targetApp = path.join(devMac, "Electron.app");
const srcApp = path.join(root, "node_modules", "electron", "dist", "Electron.app");
const stampPath = path.join(devMac, ".electron-version");

function electronVersion() {
  const j = JSON.parse(fs.readFileSync(path.join(root, "node_modules", "electron", "package.json"), "utf8"));
  return j.version;
}

function iconStamp() {
  const p = path.join(root, "electron", "assets", "icon.png");
  if (!fs.existsSync(p)) return "";
  return String(fs.statSync(p).mtimeMs);
}

function stampKey() {
  const pkg = JSON.parse(fs.readFileSync(path.join(root, "package.json"), "utf8"));
  const productName = pkg.build?.productName ?? "Exo";
  // Bump when launcher semantics change so existing apps refresh.
  return `${electronVersion()}\n${productName}\n${iconStamp()}\nwrapper-v3-exodev\n${root}`;
}

function needsRefresh() {
  if (!fs.existsSync(targetApp)) return true;
  if (!fs.existsSync(stampPath)) return true;
  const v = fs.readFileSync(stampPath, "utf8").trim();
  return v !== stampKey();
}

function buildIcns() {
  execSync(`node "${path.join(__dirname, "generate-mac-icns.js")}"`, { cwd: root, stdio: "inherit" });
  const icnsOut = path.join(root, "electron", "assets", "icon.icns");
  if (!fs.existsSync(icnsOut)) {
    throw new Error("icon.icns was not generated");
  }
  return icnsOut;
}

/**
 * Install a separate CFBundleExecutable wrapper for Dock / exo:// cold starts.
 * Keep the Mach-O binary named `Electron` so `electron` CLI + isPackaged heuristics
 * stay correct (renaming to Electron.real makes Electron think it is packaged,
 * which breaks preload paths and hides Google sign-in).
 */
function installProjectLauncher(appPath, appRoot) {
  const macOSDir = path.join(appPath, "Contents", "MacOS");
  const electronBin = path.join(macOSDir, "Electron");
  const launcherName = "ExoDev";
  const launcherBin = path.join(macOSDir, launcherName);
  const plist = path.join(appPath, "Contents", "Info.plist");

  if (!fs.existsSync(electronBin)) {
    // Recover from older broken prepare that renamed the binary to Electron.real
    const electronReal = path.join(macOSDir, "Electron.real");
    if (fs.existsSync(electronReal) && !fs.existsSync(electronBin)) {
      const st = fs.statSync(electronReal);
      if (st.isFile() && st.size > 1000) {
        fs.renameSync(electronReal, electronBin);
      }
    }
  }
  if (!fs.existsSync(electronBin)) {
    throw new Error(`Electron binary missing at ${electronBin}`);
  }
  // If Electron is still our old bash wrapper, restore from Electron.real
  const electronHead = Buffer.alloc(2);
  const fd = fs.openSync(electronBin, "r");
  fs.readSync(fd, electronHead, 0, 2, 0);
  fs.closeSync(fd);
  if (electronHead.toString("utf8") === "#!") {
    const electronReal = path.join(macOSDir, "Electron.real");
    if (!fs.existsSync(electronReal)) {
      throw new Error("Electron is a script but Electron.real is missing — delete electron/dev-macos and re-run prepare");
    }
    fs.rmSync(electronBin, { force: true });
    fs.renameSync(electronReal, electronBin);
  }

  const escapedRoot = appRoot.replace(/'/g, `'\\''`);
  const wrapper = `#!/bin/bash
# Generated by scripts/prepare-mac-dev-app.cjs — do not edit.
# Used only as CFBundleExecutable for Dock / exo://. npm still launches ./Electron.
set -euo pipefail
DIR="$(cd "$(dirname "$0")" && pwd)"
APP_ROOT='${escapedRoot}'
REAL="$DIR/Electron"

has_app_path=0
for arg in "$@"; do
  case "$arg" in
    -*|exo://*|EXO://*|-psn_*) ;;
    *)
      if [ -e "$arg" ]; then has_app_path=1; break; fi
      ;;
  esac
done

if [ "$has_app_path" -eq 0 ]; then
  exec "$REAL" "$APP_ROOT" "$@"
fi
exec "$REAL" "$@"
`;
  fs.writeFileSync(launcherBin, wrapper, { mode: 0o755 });
  execSync(`plutil -replace CFBundleExecutable -string "${launcherName}" "${plist}"`, { stdio: "inherit" });
}

/**
 * Declare exo:// in Info.plist so LaunchServices associates the scheme with this bundle.
 */
function installUrlScheme(plistPath) {
  const schemePlist = path.join(devMac, "exo-url-scheme.plist");
  fs.writeFileSync(
    schemePlist,
    `<?xml version="1.0" encoding="UTF-8"?>
<!DOCTYPE plist PUBLIC "-//Apple//DTD PLIST 1.0//EN" "http://www.apple.com/DTDs/PropertyList-1.0.dtd">
<plist version="1.0">
<array>
  <dict>
    <key>CFBundleTypeRole</key>
    <string>Editor</string>
    <key>CFBundleURLName</key>
    <string>Exo Auth Callback</string>
    <key>CFBundleURLSchemes</key>
    <array>
      <string>exo</string>
    </array>
  </dict>
</array>
</plist>
`,
    "utf8",
  );
  try {
    execSync(`plutil -remove CFBundleURLTypes "${plistPath}"`, { stdio: "ignore" });
  } catch (_) {
    /* key may not exist */
  }
  execSync(`plutil -insert CFBundleURLTypes -xml "$(cat "${schemePlist}")" "${plistPath}"`, {
    stdio: "inherit",
    shell: "/bin/bash",
  });
  fs.rmSync(schemePlist, { force: true });
}

function main() {
  if (process.platform !== "darwin") {
    return;
  }
  if (!fs.existsSync(srcApp)) {
    console.warn("prepare-mac-dev-app: node_modules/electron/dist/Electron.app not found; skip.");
    return;
  }

  execSync(`node "${path.join(__dirname, "render-icon.cjs")}"`, { cwd: root, stdio: "inherit" });

  if (!needsRefresh()) {
    // Still refresh the launcher path in case the repo moved.
    installProjectLauncher(targetApp, root);
    console.log("electron/dev-macos/Electron.app is up to date (launcher refreshed).");
    return;
  }

  const pkg = JSON.parse(fs.readFileSync(path.join(root, "package.json"), "utf8"));
  const productName = pkg.build?.productName ?? "Exo";

  console.log("Preparing branded Electron.app for macOS dev (Dock name + icon + exo:// launcher)…");
  fs.rmSync(devMac, { recursive: true, force: true });
  fs.mkdirSync(devMac, { recursive: true });
  fs.mkdirSync(targetApp, { recursive: true });
  execSync(`rsync -a --delete "${srcApp}/" "${targetApp}/"`, { stdio: "inherit" });

  const plist = path.join(targetApp, "Contents", "Info.plist");
  execSync(`plutil -replace CFBundleDisplayName -string "${productName}" "${plist}"`, { stdio: "inherit" });
  execSync(`plutil -replace CFBundleName -string "${productName}" "${plist}"`, { stdio: "inherit" });
  // Distinct from packaged com.exo.app so LaunchServices can tell them apart.
  execSync(`plutil -replace CFBundleIdentifier -string "com.exo.app.dev" "${plist}"`, { stdio: "inherit" });

  const icns = buildIcns();
  const resDir = path.join(targetApp, "Contents", "Resources");
  fs.copyFileSync(icns, path.join(resDir, "icon.icns"));
  execSync(`plutil -replace CFBundleIconFile -string "icon" "${plist}"`, { stdio: "inherit" });

  installUrlScheme(plist);
  installProjectLauncher(targetApp, root);

  try {
    execSync(`xattr -cr "${targetApp}"`, { stdio: "inherit" });
  } catch (_) {
    /* ignore */
  }

  fs.writeFileSync(stampPath, stampKey(), "utf8");
  console.log(`Done. Dock will show "${productName}"; exo:// cold-starts this repo.`);
}

main();

#!/usr/bin/env node
/**
 * Build datasuite/.env.server (DB + admin hash; no plaintext password in file).
 *
 * Reads cloud-node/.env for DB_* unless DATASUITE_DB_* overrides.
 * Set DATASUITE_ADMIN_PASSWORD to keep an existing login password.
 */
const crypto = require("crypto");
const fs = require("fs");
const path = require("path");

const repoRoot = path.join(__dirname, "..", "..");
const cloudEnvPath = path.join(repoRoot, "cloud-node", ".env");
const dbEnvPath = path.join(__dirname, "..", ".env.db");
const outPath = path.join(__dirname, "..", ".env.server");
const bcrypt = require(path.join(repoRoot, "cloud-node", "node_modules", "bcryptjs"));

function parseEnvFile(filePath) {
  const out = {};
  if (!fs.existsSync(filePath)) {
    return out;
  }
  for (const line of fs.readFileSync(filePath, "utf8").split("\n")) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#") || !trimmed.includes("=")) {
      continue;
    }
    const [key, ...rest] = trimmed.split("=");
    out[key.trim()] = rest.join("=").trim();
  }
  return out;
}

function main() {
  const cloud = parseEnvFile(cloudEnvPath);
  const dbLocal = parseEnvFile(dbEnvPath);
  const merged = { ...cloud, ...dbLocal };

  const dbHost = process.env.DB_HOST || merged.DB_HOST;
  const dbName = process.env.DB_NAME || merged.DB_NAME;
  const dbUser =
    process.env.DATASUITE_DB_USER || merged.DATASUITE_DB_USER || merged.DB_USER;
  const dbPassword =
    process.env.DATASUITE_DB_PASSWORD ||
    merged.DATASUITE_DB_PASSWORD ||
    merged.DB_PASSWORD;

  if (!dbHost || !dbName || !dbUser || !dbPassword) {
    console.error("Missing DB_* — set in cloud-node/.env or DATASUITE_DB_* env overrides.");
    process.exit(1);
  }

  const adminPassword =
    process.env.DATASUITE_ADMIN_PASSWORD || crypto.randomBytes(16).toString("base64url");
  const adminHash = bcrypt.hashSync(adminPassword, 10).replace(/^\$2b\$/, "$2y$");

  const lines = [
    "# Generated by datasuite/scripts/generate-server-env.cjs — do not commit",
    `DB_HOST=${dbHost}`,
    `DB_PORT=${process.env.DB_PORT || cloud.DB_PORT || "3306"}`,
    `DB_NAME=${dbName}`,
    `DB_USER=${dbUser}`,
    `DB_PASSWORD=${dbPassword}`,
    `ADMIN_PASSWORD_HASH=${adminHash}`,
    "SESSION_NAME=datasuite_session",
    "",
  ];

  fs.writeFileSync(outPath, lines.join("\n"), { mode: 0o600 });
  console.log(`Wrote ${outPath}`);
  if (!process.env.DATASUITE_ADMIN_PASSWORD) {
    console.error("\nNew team login password (save in password manager, shown once):\n");
    console.error(adminPassword);
  }
}

main();
